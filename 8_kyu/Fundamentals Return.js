function add(a, b) {
    return a + b
}

function divide(a, b) {
    return a / b
}

function multiply(a, b) {
    return a * b
}

function mod(a, b) {
    return a % b
}

function exponent(a, b) {
    return Math.pow(a, b)
}

function subt(a, b) {
    return a - b
}

add(1, 2)
multiply(1, 2)
divide(2, 1)
mod(1, 2)
exponent(1, 2)
subt(1, 2)